public class DemoDoWhileLoop {

    public static void main(String[] args) {

        // do {
        //       CODE
        //      increment/decrement;
        //   }    while(condition);

        int i=0;

        do{
            System.out.println("Demo do while loop..."+i);
            i++;
        }while (i<5);

    }


}
