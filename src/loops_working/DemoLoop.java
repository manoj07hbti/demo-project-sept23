package loops_working;

public class DemoLoop {

    public static void main(String[] args) {


     /* SYNTAX: FOR ( star _ condition; conditional_ expression; increment){
      //code
     } */
        for (int i = 1; i <= 5; i++) {

            System.out.println(" HELLO DOCTOR" + i);

        }

        // DECREMENT FOR LOOP... SYNTAX : FOR ( star _ condition; conditional_expression; decrement{
        //                                 //code
        //                             }

        for(int j=5; j>0; j--){
            System.out.println( "Hello world using decrement for loop........." +j);
        }
















    }
}