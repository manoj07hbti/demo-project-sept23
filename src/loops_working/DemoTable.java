package loops_working;

public class DemoTable {
    public static void main(String[] args) {
        //table of 5 using for loop
        int number = 11;
        for (int i = 1; i <= 10; i++) {
            System.out.println(number + " X " + i + " = " + number * i);
        }

        //table of 10 using for loop
        number = 5;
        for (int i = 1; i <= 10; i++) {
            System.out.println(number + " X " + i + " = " + number * i);
        }


    }
}
