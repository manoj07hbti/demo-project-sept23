package loops_working;

public class Loops {

    public static void main(String[] args) {

        // Print table of 5 and 10 using for loop;

        int number=5;

        for(int i=1; i<=10; i++){

            System.out.println(number + " X " +i+ " = " +number*i);

        }
        // table 10;

        number=10;

        for(int i=1; i<=10; i++){

            System.out.println(number + " X " +i+ " = " +number*i);
        }
    }
}
