package loop_training;

public class Assignment_Forloop {
    public static void main(String[] args) {
        /*Print table of 5 and 10 using for loop */

        int number = 5;

        for (int a = 1; a <= 10; a++) {

            System.out.println(number + " x " + a + " = " + number * a);
        }
        /*Print table of 5 and 10 using for loop */


        int num = 10;

        for (int b = 1; b <= 10; b++) {

            System.out.println(num + " x " + b + " = " + num * b);
        }








    }


}
