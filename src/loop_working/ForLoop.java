package loop_working;

public class ForLoop {

    public static void main(String[] args) {

        // Print table of 5 and 10 using for loop

        int number=5;
        for(int i =1; i<=10; i++) {
            System.out.println(number +" X " +i+" = " +number*i);}

// Print table of 10

            number =10;
            for(int i =1; i<=10; i++) {
                System.out.println(number +" X " +i+" = " +number*i);}
        }
    }

