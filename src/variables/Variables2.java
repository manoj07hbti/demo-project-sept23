package variables;

public class Variables2 {
    public static void main(String[] args) {
        //addition of numbers
        int x=5;
        int y=2;
        int value=x+y;
        System.out.println("addition of numbers :"+value);

        //subtraction of numbers
        value=x-y;
        System.out.println("subtraction  of numbers :"+value);

        //multiplication of numbers
        value=x*y;
        System.out.println("multiplication of numbers :"+value);

        //division of numbers
        double divide=(double)x/y;
        System.out.println("division of numbers :"+divide);

        //square of numbers
        int a= 3;
        int result=a*a;
        System.out.println("square of numbers :"+result);

        //cube of numbers
        a=5;
        result=a*a*a;
        System.out.println("cube of numbers :"+result);

        //5percent of a number
        a=25;
        double percentage=(double)a*5/100;
        System.out.println("percentage of a number :"+percentage);

    }

}
